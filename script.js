function createArtworkCard(item) {
  return `
    <article class="artwork-card" data-reveal>
      <div class="artwork-wrapper">
        <img class="artwork-image ${item.layout}" src="${item.image}" alt="${item.alt}">
        <div class="description">
  <p>${item.description}</p>
</div>
      </div>
      <div class="caption">
        <div class="caption-title">${item.title}</div>
        <div class="caption-size">${item.size}</div>
      </div>
    </article>
  `;
}

function renderArtwork() {
  const container = document.getElementById("artworkList");
  if (!container) return;

  container.innerHTML = ARTWORKS.map(row => `
    <div class="artwork-row ${row.row}">
      ${row.items.map(createArtworkCard).join("")}
    </div>
  `).join("");
}

function renderExhibitions() {
  const container = document.getElementById("exhibitionsList");
  if (!container) return;

  container.innerHTML = EXHIBITIONS.map(item => `
    <div class="exhibition-item" data-reveal>
      <div class="exhibition-date">${item.date}</div>
      <div class="exhibition-title">${item.title}</div>
      <div class="exhibition-location">${item.location}</div>
      <div class="exhibition-type">${item.type}</div>
    </div>
  `).join("");
}

function renderCarousel(selector, images, labelPrefix) {
  const container = document.querySelector(selector);
  if (!container) return;

  container.innerHTML = images.map((src, index) => `
    <div class="carousel-cell">
      <img src="${src}" alt="${labelPrefix} ${index + 1}">
    </div>
  `).join("");
}

function setupHamburger() {
  const hamburger = document.getElementById("hamburger");
  const navLinks = document.getElementById("navLinks");

  if (!hamburger || !navLinks) return;

  hamburger.addEventListener("click", () => {
    const isOpen = navLinks.classList.toggle("show");
    hamburger.classList.toggle("active", isOpen);
    hamburger.setAttribute("aria-expanded", String(isOpen));
  });

  navLinks.querySelectorAll("a").forEach(link => {
    link.addEventListener("click", () => {
      navLinks.classList.remove("show");
      hamburger.classList.remove("active");
      hamburger.setAttribute("aria-expanded", "false");
    });
  });
}

function setupNavHideOnScroll() {
  const nav = document.querySelector(".nav");
  if (!nav) return;

  let lastScrollTop = 0;

  window.addEventListener("scroll", () => {
    const scrollTop = window.pageYOffset || document.documentElement.scrollTop;

    if (scrollTop > lastScrollTop && scrollTop > 100) {
      nav.style.top = "-100px";
    } else {
      nav.style.top = "0";
    }

    lastScrollTop = scrollTop;
  });
}

function setupRevealAnimation() {
  const elements = document.querySelectorAll("[data-reveal]");
  if (!elements.length) return;

  const observer = new IntersectionObserver(
    entries => {
      entries.forEach(entry => {
        if (entry.isIntersecting) {
          setTimeout(() => entry.target.classList.add("visible"), 100);
        } else if (
          entry.boundingClientRect.top > window.innerHeight + 100 ||
          entry.boundingClientRect.bottom < -100
        ) {
          entry.target.classList.remove("visible");
        }
      });
    },
    { threshold: 0.2 }
  );

  elements.forEach(el => observer.observe(el));
}

function setupArtworkTouchToggle() {
  const isTouchDevice = "ontouchstart" in window || navigator.maxTouchPoints > 0;
  if (!isTouchDevice) return;

  document.querySelectorAll(".artwork-wrapper").forEach(wrapper => {
    wrapper.addEventListener("click", event => {
      event.preventDefault();
      event.stopPropagation();

      const currentDescription = wrapper.querySelector(".description");

      document.querySelectorAll(".description.show").forEach(desc => {
        if (desc !== currentDescription) desc.classList.remove("show");
      });

      currentDescription?.classList.toggle("show");
    });
  });

  document.addEventListener("click", event => {
    if (!event.target.closest(".artwork-wrapper")) {
      document.querySelectorAll(".description.show").forEach(desc => {
        desc.classList.remove("show");
      });
    }
  });
}

function createCarousel(selector, options = {}) {
  const element = document.querySelector(selector);
  if (!element) return null;

  const flkty = new Flickity(element, {
    cellAlign: "center",
    contain: true,
    wrapAround: true,
    pageDots: false,
    prevNextButtons: false,
    ...options
  });

  let autoSlide = setInterval(() => flkty.next(), 3000);

  const stopAutoSlide = () => clearInterval(autoSlide);

  flkty.on("pointerDown", stopAutoSlide);
  flkty.on("dragStart", stopAutoSlide);
  flkty.on("staticClick", stopAutoSlide);

  return flkty;
}

function setupContactForm() {
  const form = document.getElementById("contact-form");
  if (!form) return;

  form.addEventListener("submit", async event => {
    event.preventDefault();

    try {
      const data = new FormData(form);

      await fetch(CONTACT_ENDPOINT, {
        method: "POST",
        body: data
      });

      alert("The message has been sent :) Thank you!");
      form.reset();
    } catch (error) {
      alert("Something went wrong. Please try again :(");
      console.error(error);
    }
  });
}

function setupSmoothScroll() {
  document.querySelectorAll('a[href^="#"]').forEach(anchor => {
    anchor.addEventListener("click", event => {
      const targetSelector = anchor.getAttribute("href");
      const target = document.querySelector(targetSelector);

      if (!target) return;

      event.preventDefault();
      target.scrollIntoView({
        behavior: "smooth",
        block: "start"
      });
    });
  });
}

function init() {
  renderArtwork();
  renderExhibitions();
  renderCarousel(".carousel-1", GALLERY_IMAGES, "Gallery");
  renderCarousel(".carousel-2", SKETCH_IMAGES, "Sketch");

  setupHamburger();
  setupNavHideOnScroll();
  setupRevealAnimation();
  setupArtworkTouchToggle();
  setupContactForm();
  setupSmoothScroll();

  createCarousel(".carousel-1");
  createCarousel(".carousel-2");
}

